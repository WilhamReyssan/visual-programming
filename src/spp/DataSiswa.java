    public void edit(){
         try{
            nis=jTextField1.getText();
            angkatan=jTextField4.getText().toString();
            hp=jTextField6.getText().toString();
            nama=jTextField2.getText().toString();
            kelas=jTextField5.getText().toString();
            alamat=jTextArea1.getText().toString();
            String cari=jTextField8.getText().toString();
            
            String kelamin="-";
            if (jRadioButton1.isSelected()){
                kelamin="L";
            }
            else if (jRadioButton2.isSelected()){
                kelamin="P";
            }
            String status="-";
            int pil;
            pil = jComboBox1.getSelectedIndex();
            switch (pil) {
                 case 0:
                    status="Reguler";
                    break;
                case 1:
                    status="Anak Guru";
                    break;
                case 2:
                    status="KIP";
                    break;
                case 3:
                    status="Tahfidz 1";
                    break;
                case 4:
                    status="Tahfidz 2";
                    break;
                case 5:
                    status="Tahfidz 3";
                    break;
               
                default:
                JOptionPane.showMessageDialog(null, "Reguler");
                break;
            }
            
            String sqli = "UPDATE siswa SET nis='"+nis+"',"
                    +"nama='"+nama+"',"
                    +"kelamin='"+kelamin+"',"
                    +"angkatan='"+angkatan+"',"
                    +"kelas='"+kelas+"',"
                    +"alamat='"+alamat+"',"
                    +"hp='"+hp+"',"
                    +"status='"+status+"' WHERE nis='"+cari+"'";
          
          stat.executeUpdate(sqli);
            stat.close();
            System.out.println("Berhasil Memasukan Data");
        }catch(Exception e){
            System.err.println("gagal Memasukan Data");
            
        }
    }
             
