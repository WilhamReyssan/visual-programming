    public boolean cek (){
        sql = "SELECT* FROM dropdown where tahun_ajar= '"+ jTextField10.getText()+ "'" ;        
        try {                        
           stat=con.createStatement();                 
           stat.execute(sql);                        
           rs=stat.getResultSet();
           while (rs.next()) {
                String id_sA=rs.getString("tahun_ajar");
                if(id_sA.equals(jTextField10.getText())){
                   System.err.println("gagal Memasukan Data");
                    JOptionPane.showMessageDialog(null, "Data Sudah Ada","Informasi",JOptionPane.INFORMATION_MESSAGE);
                    return true;
                     
                }        
            }                      
        }catch (SQLException ex) 
        {            
            JOptionPane.showMessageDialog(null, ex);
        }
        return false;
    }  
    
